    private void invTablMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_invTablMouseClicked

        int r=invTabl.getSelectedRow();
        
        inNo.setText(invTabl.getValueAt(r, 0).toString());
        jobId.setSelectedItem(invTabl.getValueAt(r, 2).toString());
        Qref.setText(invTabl.getValueAt(r, 4).toString());
        jbAmnt.setText(invTabl.getValueAt(r, 5).toString());
                
        String dt=invTabl.getValueAt(r, 1).toString();
        
        
        try
        {
             DateFormat df= new SimpleDateFormat("yyyy-MM-dd");//pattern must be the same as in table
             Date invDt;
               
        
            
            invDt=df.parse(dt);
            
             inDt.setDate(invDt);
        }
        catch(Exception e)
        {
            System.out.println("error in date to form:"+e);   
        }
    }//GEN-LAST:event_invTablMouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String ino=inNo.getText();
        String dt=((JTextField)inDt.getDateEditor().getUiComponent()).getText();
        String jid=jobId.getSelectedItem().toString();
        String ref=Qref.getText();
        String amnt=jbAmnt.getText();
        String cli=cl.getText();
        
        String insert="INSERT INTO invlb VALUES('"+ino+"','"+dt+"','"+jid+"','"+cli+"','"+ref+"','"+amnt+"')";
        
        try 
        {
          pst=con.prepareStatement(insert);
          pst.execute();
          
          JOptionPane.showMessageDialog(null,"Invoice Saved!");
          tableload();
        } 
        catch (Exception e) 
        {
            System.out.println("Error in inserting:"+e);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

