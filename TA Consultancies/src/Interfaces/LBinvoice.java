    public void tableload()
    {
    
        String load="SELECT * from invlb";
        
        try 
        {
            pst=con.prepareStatement(load);
            rst=pst.executeQuery();
            
            invTabl.setModel(DbUtils.resultSetToTableModel(rst));
            
        } 
        catch (SQLException e) 
        {
            System.out.println("Error:"+e);
        }
    
   }
    
     public void fillJob()
    {
        try {
            String getID="SELECT LBID FROM joblb ";
    
            pst=con.prepareStatement(getID);
            rst=pst.executeQuery();
            
            
            while (rst.next()){
                  
                
                  jobId.addItem(rst.getObject("LBID").toString());
            
            
            }
        } catch (SQLException e) {
            
            System.out.println("Error:"+e);
        }
    }
     
