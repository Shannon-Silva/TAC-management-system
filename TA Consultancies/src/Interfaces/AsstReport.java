    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed

        String date1=((JTextField)d1.getDateEditor().getUiComponent()).getText();
        String date2=((JTextField)d2.getDateEditor().getUiComponent()).getText();
        
        System.out.println("d1:"+date1+", d2:"+date2);
                
         
        try
        {
            JasperDesign jd= JRXmlLoader.load("C:\\Users\\MAX\\Documents\\NetBeansProjects\\TA Consultancies\\src\\Reports\\purchsReport.jrxml");
            String sql="SELECT a.assetNo,a.vendName,a.price,a.purchDate,a.category,a.location,m.wPeriod, m.wtimeUnit FROM maintain m INNER JOIN assets a ON m.assetNo = a.assetNo WHERE a.purchDate BETWEEN  '"+date1+"' AND '"+date2+"'";
            JRDesignQuery newquery=new JRDesignQuery();
            newquery.setText(sql);
            jd.setQuery(newquery);
            
            JasperReport jr= JasperCompileManager.compileReport(jd);
            jp= JasperFillManager.fillReport(jr,null, con);
            JasperViewer.viewReport(jp,false);
        }
        catch(Exception e)
        {
            System.out.println("Error getting report: "+e);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

